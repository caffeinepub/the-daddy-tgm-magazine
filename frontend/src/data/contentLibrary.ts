// ============================================================
// TYPE DEFINITIONS
// ============================================================

export interface SpoofMoviePoster {
  type: 'spoofMoviePoster';
  title: string;
  tagline: string;
  cast: string[];
  director: string;
  asciiArt: string;
  rating: string;
  year: string;
  studio: string;
}

export interface ComicStrip {
  type: 'comicStrip';
  title: string;
  panels: Array<{
    character: string;
    dialogue: string;
    action?: string;
  }>;
  caption?: string;
}

export interface FakeProductAd {
  type: 'fakeProductAd';
  productName: string;
  slogan: string;
  bodyCopy: string;
  price?: string;
  finePrint?: string;
}

export interface SpoofReview {
  type: 'spoofReview';
  movieTitle: string;
  rating: number;
  reviewText: string;
  reviewer: string;
  publication: string;
}

export interface LetterToEditor {
  type: 'letterToEditor';
  name: string;
  location: string;
  letterBody: string;
  editorNote?: string;
}

export interface BackPageItem {
  type: 'backPage';
  title: string;
  content: string;
  subContent?: string;
  style: 'ad' | 'joke' | 'classified' | 'announcement';
}

export type ContentItem =
  | SpoofMoviePoster
  | ComicStrip
  | FakeProductAd
  | SpoofReview
  | LetterToEditor
  | BackPageItem;

